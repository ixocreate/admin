declare(strict_types=1);

namespace KiwiSuite\Admin\Middleware;

use KiwiSuite\ApplicationHttp\Middleware\SegmentMiddlewarePipe;
use Psr\Http\Message\ResponseInterface;
use Psr\Http\Message\ServerRequestInterface;
use Psr\Http\Server\MiddlewareInterface;
use Psr\Http\Server\RequestHandlerInterface;

class AdminMiddleware implements MiddlewareInterface
{
    /**
     * @var SegmentMiddlewarePipe
     */
    private $segmentMiddlewarePipe;

    public function __construct(SegmentMiddlewarePipe $segmentMiddlewarePipe)
    {
        $this->segmentMiddlewarePipe = $segmentMiddlewarePipe;
    }

    /**
     * Process an incoming server request and return a response, optionally delegating
     * response creation to a handler.
     * @param ServerRequestInterface $request
     * @param RequestHandlerInterface $handler
     * @return ResponseInterface
     */
    public function process(ServerRequestInterface $request, RequestHandlerInterface $handler): ResponseInterface
    {
        return $this->segmentMiddlewarePipe->handle($request);
    }
}
